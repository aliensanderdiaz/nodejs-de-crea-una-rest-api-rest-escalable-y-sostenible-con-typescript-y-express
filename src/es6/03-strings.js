let hello = 'Hello';
let world = 'World';
let epicPhrase = hello + ' ' + world + '!';
console.log(epicPhrase);

// Template Literals
let epicPhrase2 = `${hello} ${world}!`;
console.log(epicPhrase2);

// Multi-line strings

let lorem = 'Esto es un string\n' + 'Esto es en otra linea';
console.log(lorem)

let lorem2 = `Esto es un string
Esto es en otra linea`;
console.log(lorem2)